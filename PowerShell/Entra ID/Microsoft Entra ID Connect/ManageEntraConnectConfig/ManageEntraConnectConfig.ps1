function Show-Help {
    Write-Host "Usage: .\ManageEntraConnectConfig.ps1"
}

# Check if running as administrator
$adminRights = ([Security.Principal.WindowsPrincipal] [Security.Principal.WindowsIdentity]::GetCurrent()).IsInRole([Security.Principal.WindowsBuiltInRole]::Administrator)
if (-not $adminRights) {
    Write-Host "Please run this script as an administrator."
    Start-Sleep -Seconds 3
    Start-Process powershell.exe -ArgumentList "-NoProfile -ExecutionPolicy Bypass -File `"$($MyInvocation.MyCommand.Path)`"" -Verb RunAs
    Exit
}

function Set-AuditEventLogging {
    param (
        [string]$Value
    )
    try {
        $registryPath = 'HKLM:\SOFTWARE\Microsoft\Azure AD Connect'
        $valueName = 'AuditEventLogging'
        if (!(Test-Path $registryPath)) {
            New-Item -Path $registryPath -Force -ErrorAction Stop
        }
        Set-ItemProperty -Path $registryPath -Name $valueName -Value $Value -ErrorAction Stop
        $currentValue = (Get-ItemProperty -Path $registryPath -Name $valueName).$valueName
        if ($currentValue -eq $Value) {
            Write-Host "Successfully set $valueName to $Value"
        } else {
            Write-Error "Failed to set $valueName to $Value. Current value is $currentValue"
        }
    } catch {
        Write-Error "Failed to set the AuditEventLogging setting: $_"
    }
}

function Show-Menu {
    Clear-Host
    Write-Host "========================================="
    Write-Host "       Microsoft Entra Connect Config"
    Write-Host "========================================="
    Write-Host ""
    Write-Host "1. Enable AuditEventLogging"
    Write-Host "2. Disable AuditEventLogging"
    Write-Host ""
    Write-Host "0. Exit"
    Write-Host ""
    $choice = Read-Host "Enter your choice"
    return $choice
}

if (-not $Action) {
    $Action = Show-Menu
}

switch ($Action) {
    '0' {
        exit 1
    }
    '1' {
        Set-AuditEventLogging -Value 1
    }
    '2' {
        Set-AuditEventLogging -Value 0
    }
    default {
        Show-Help
        exit 1
    }
}
